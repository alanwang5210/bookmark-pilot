export function detectBrowserLanguage() {
  const chromeLanguage = globalThis.chrome?.i18n?.getUILanguage?.();
  const navigatorLanguage = globalThis.navigator?.language;
  const raw = chromeLanguage || navigatorLanguage || "en";
  return raw.toLowerCase().startsWith("zh") ? "zh-CN" : "en";
}

export function getEffectiveLanguage(settingsOrLanguage = "auto") {
  const rawLanguage =
    typeof settingsOrLanguage === "string"
      ? settingsOrLanguage
      : settingsOrLanguage?.ui?.language || "auto";

  if (!rawLanguage || rawLanguage === "auto") {
    return detectBrowserLanguage();
  }

  return rawLanguage.toLowerCase().startsWith("zh") ? "zh-CN" : "en";
}

export function t(language, key, vars = {}) {
  const effectiveLanguage = getEffectiveLanguage(language);
  const dictionary = MESSAGES[effectiveLanguage] || MESSAGES.en;
  const template = dictionary[key] || MESSAGES.en[key] || key;
  return template.replace(/\{(\w+)\}/g, (_, name) => String(vars[name] ?? ""));
}

export function getLanguageOptions() {
  return [
    { value: "auto", labelKey: "optionsLanguageAuto" },
    { value: "zh-CN", labelKey: "optionsLanguageZh" },
    { value: "en", labelKey: "optionsLanguageEn" }
  ];
}
